package PC1.problema1;

public class Reservas {
    private int numreserva;
    private float montoreserva;
    private String[] listareservas;

    public int getNumreserva() {
        return numreserva;
    }

    public void setNumreserva(int numreserva) {
        this.numreserva = numreserva;
    }

    public float getMontoreserva() {
        return montoreserva;
    }

    public void setMontoreserva(float montoreserva) {
        this.montoreserva = montoreserva;
    }

    public String[] getListareservas() {
        return listareservas;
    }

    public void setListareservas(String[] listareservas) {
        this.listareservas = listareservas;
    }
}
